<template>
    <div>
        <h1 class="alert alert-success">CompositionApi 입니다</h1>

        <h3>Counter:{{ counter }}</h3>
        <button @click="numIncre">증가</button>
        <button v-on:click="numDecre">감소</button>
    </div>
</template>

<script>
import { onMounted, ref } from 'vue';

export default {
    setup() {
        const counter = ref(0);
        const numIncre = () => counter.value++;
        const numDecre = () => counter.value--;


        onMounted(()=>{
            console.log("컴포넌트가 마운트됨");
        })

        return {
            // return에 변수나 메서드명 등록 안하면 사용 불가
            counter,
            numIncre,
            numDecre
        }
    }
}
</script>

